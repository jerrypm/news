import SwiftUI

extension Color {
    // MARK: Category - Green
    public static var jade: Color {
        return Color(SC.jade.value)
    }
    
    // MARK: Category - Blue
    public static var malibu: Color {
        return Color(SC.malibu.value)
    }
    
    // MARK: Category - Red
    public static var coral: Color {
        return Color(SC.coral.value)
    }
    
    // MARK: Category - Background
    public static var backgroundDefault: Color {
        return Color(SC.backgroundDefault.value)
    }
    
    // MARK: Category - Text
    public static var textPrimary: Color {
        return Color(SC.textPrimary.value)
    }
}


